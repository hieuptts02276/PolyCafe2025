    private void sendNotificationWithIcon(String title, String message, String iconPath) {
    if (!SystemTray.isSupported()) {
        System.out.println("❌ System Tray không được hỗ trợ!");
        return;
    }

    try {
        URL imageURL = getClass().getResource(iconPath);
        if (imageURL == null) {
            System.out.println("❌ Không tìm thấy icon tại: " + iconPath);
            return;
        }

        Image originalImage = Toolkit.getDefaultToolkit().createImage(imageURL);
        MediaTracker tracker = new MediaTracker(new java.awt.Container());
        tracker.addImage(originalImage, 0);
        tracker.waitForAll();

        int width = originalImage.getWidth(null);
        int height = originalImage.getHeight(null);
        BufferedImage fadedImage = new BufferedImage(width, height, BufferedImage.TYPE_INT_ARGB);
        Graphics2D g2d = fadedImage.createGraphics();
        g2d.setComposite(AlphaComposite.getInstance(AlphaComposite.SRC_OVER, 0.3f));
        g2d.drawImage(originalImage, 0, 0, null);
        g2d.dispose();

        TrayIcon trayIcon = new TrayIcon(fadedImage, "Thông báo");
        trayIcon.setImageAutoSize(true);
        trayIcon.setToolTip("Ứng dụng quản lý quán cafe");

        SystemTray tray = SystemTray.getSystemTray();
        tray.add(trayIcon);
        trayIcon.displayMessage(title, message, TrayIcon.MessageType.INFO);

        new java.util.Timer().schedule(new java.util.TimerTask() {
            @Override
            public void run() {
                tray.remove(trayIcon);
            }
        }, 5000);

    } catch (Exception e) {
        e.printStackTrace();
    }
}


