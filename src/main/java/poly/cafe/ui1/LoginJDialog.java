@Override
public void login() {
    String username = txtUsername.getText();
    String password = txtPassword.getText();

    UserDAO dao = new UserDAOImpl();
    User user = dao.findById(username);
    if (user == null) {
        XDialog.alert("Sai tên đăng nhập!");
    } else if (!password.equals(user.getPassword())) {
        XDialog.alert("Sai mật khẩu đăng nhập!");
    } else if (!user.isEnabled()) {
        XDialog.alert("Tài khoản của bạn đang tạm dừng!");
    } else {
        XAuth.user = user;
        this.dispose();
    }
}
private void sendNotification(String title, String message) {
    if (!SystemTray.isSupported()) {
        System.out.println("System tray not supported!");
        return;
    }

    SystemTray tray = SystemTray.getSystemTray();
    Image image = Toolkit.getDefaultToolkit().createImage(getClass().getResource("/icons/app_icon.png")); // Đảm bảo đường dẫn hợp lệ

    TrayIcon trayIcon = new TrayIcon(image, "App Notification");
    trayIcon.setImageAutoSize(true);
    trayIcon.setToolTip("Thông báo từ ứng dụng quản lý quán cafe");

    try {
        tray.add(trayIcon);
        trayIcon.displayMessage(title, message, TrayIcon.MessageType.INFO);
        tray.remove(trayIcon);
    } catch (AWTException e) {
        e.printStackTrace();
    }
}

