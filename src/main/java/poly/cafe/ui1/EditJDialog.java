    private void updateUserInfo() {
        if (Auth.isLogin()) {
            User currentUser = Auth.user; // Lấy thông tin người dùng hiện tại

            String newFullname = txtName.getText().trim();
            boolean newIsManager = rdoQuanLi.isSelected();

            // Validate dữ liệu nhập
            if (newFullname.isEmpty()) {
                XDialog.alert( "Họ và tên không được để trống!");
                return;
            }

            // Tạo một đối tượng User mới với các thông tin đã chỉnh sửa
            User updatedUser = new User();
            updatedUser.setUsername(currentUser.getUsername()); // Tên đăng nhập không đổi
            updatedUser.setPassword(currentUser.getPassword()); // Giữ nguyên mật khẩu (nếu không có trường đổi mật khẩu)
            updatedUser.setEnabled(currentUser.isEnabled());     // Giữ nguyên trạng thái hoạt động
            updatedUser.setFullname(newFullname);              // Cập nhật họ và tên
            updatedUser.setPhoto(currentUser.getPhoto());      // Giữ nguyên ảnh (nếu không có chức năng đổi ảnh)

            // Chỉ cập nhật vai trò nếu người dùng hiện tại là quản lý
            if (Auth.isManager()) {
                updatedUser.setManager(newIsManager);
            } else {
                updatedUser.setManager(currentUser.isManager()); // Giữ nguyên vai trò nếu không phải quản lý
            }

            try {
                userDAO.update(updatedUser); // Gọi DAO để cập nhật vào CSDL
                Auth.user = updatedUser;     // Cập nhật lại đối tượng người dùng trong Auth

                XDialog.alert( "Cập nhật thông tin thành công!");
                this.dispose(); // Đóng cửa sổ sau khi cập nhật thành công
            } catch (Exception e) {
                XDialog.alert( "Cập nhật thông tin thất bại: " + e.getMessage());
                e.printStackTrace(); // In lỗi ra console để debug
            }
        }
    }

