    private void loadUserInfo() {
    if (!Auth.isLogin()) {
        // Nếu chưa đăng nhập, mở LoginJDialog
        LoginJDialog login = new LoginJDialog(null, true); // Hoặc dùng SwingUtilities.getWindowAncestor(this)
        login.setVisible(true);

        // Sau khi LoginJDialog đóng, kiểm tra lại trạng thái đăng nhập
        if (!Auth.isLogin()) {
            XDialog.alert("Chưa đăng nhập. Hệ thống sẽ đóng.");
            this.dispose(); // Thoát nếu vẫn chưa đăng nhập
            return;
        }
    }

    // Nếu đã đăng nhập (hoặc đăng nhập xong)
    User currentUser = Auth.user;

    txtUsername.setText(currentUser.getUsername());
    txtName.setText(currentUser.getFullname());
    txtUsername.setEditable(false);

    if (currentUser.isManager()) {
        rdoQuanLi.setSelected(true);
    } else {
        rdoNhanVien.setSelected(true);
    }

    if (!Auth.isManager()) {
        rdoNhanVien.setEnabled(false);
        rdoQuanLi.setEnabled(false);
        jLabel3.setText("Vai trò (không thể chỉnh sửa)");
    }
}


